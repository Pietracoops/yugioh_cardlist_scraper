

class Card:
    def __init__(self):
        self.name = ""
        self.attribute = ""
        self.link = ""
        self.level = ""
        self.attack = ""
        self.defense = ""
        self.type = ""
        self.spell_attribute = ""
        self.card_text = ""


    def clear(self):
        self.name = ""
        self.attribute = ""
        self.link = ""
        self.level = ""
        self.attack = ""
        self.defense = ""
        self.type = ""
        self.spell_attribute = ""
        self.card_text = ""